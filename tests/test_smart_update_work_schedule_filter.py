from smart_event_update import _looks_like_work_schedule_notice


def test_work_schedule_filter_keeps_registered_lecture_at_museum():
    text = (
        "Продолжается регистрация на лекцию\n\n"
        "15.05 | О чём мечтали\n"
        "в советском Калининграде,\n"
        "куда стремились\n"
        "и куда попали\n"
        "#лекция #анонс\n\n"
        "Лекция Ирины Литвинович в рамках фестиваля «80 историй о главном» "
        "расскажет, как здесь мечтали о море, парусах, акваланге, космосе, "
        "дальних странах и собственной большой судьбе.\n\n"
        "по регистрации\n\n"
        "15 мая 16:00\n"
        "Лекторий ОКЕАНиЯ\n"
        "Музей Мирового океана, наб. Петра Великого 1, #Калининград"
    )

    assert _looks_like_work_schedule_notice(
        "О чём мечтали в советском Калининграде, куда стремились и куда попали",
        text,
    ) is False


def test_work_schedule_filter_still_skips_plain_museum_hours_notice():
    text = (
        "График работы музея в праздничные дни\n\n"
        "1 мая музей работает с 10:00 до 18:00.\n"
        "2 мая санитарный день."
    )

    assert _looks_like_work_schedule_notice("График работы музея", text) is True


